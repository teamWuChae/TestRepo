	@Override
	public void run() {
		dropNotes(this.titleName);
		//결과
		if(musicEnd) {
			if(score <= 10000) rank = "F"; 
			else if(score <= 50000) rank = "C"; 
			else if(score <= 70000) rank = "B"; 
			else if(score <= 100000) rank = "A"; 
			else if(score <= 130000) rank = "S"; 
			
			if(score >= bestScore) bestScore = score;
		}
	}
	
	public void close() {
		gameMusic.close();
		result.start();
		musicEnd = true;
		this.interrupt();
	}
	
